                <h2>Storks</h2>
                <h3>By : Zhurba Kseniya</h3>
                <hr>
                <p>
                    Late one night, on the 19th of April 2014, in a rural Ukrainian village, a loud sound woke Oksana up in the middle of the night. <br>
                    “Could it be Easter fireworks?” she wondered. Easter Sunday was the brightest and happiest holiday for her. <br>
                </p>
                <p>
                    But these weren’t fireworks; this was war. Volleys of automatic gunfire, grenades and explosions made her heart pound. <br>
                    Was this real? She went to the church and prayed. She could not have imagined the suffering ahead. <br>
                </p>
                <p>
                    An artillery shell fell in their own garden. Fear filled her stomach. She found her daughters and hugged them for a long time as she cried. <br>
                </p>
                <p>
                    She couldn’t remember if the rain had ever made people so happy, but the shooting stopped when it rained. And that was enough. <br>
                </p>
                <p>
                    For their children’s safety, they decided to leave. But how to pack for the trip? They needed documents, money, and clothes. <br>
                    And what about the library of books collected over generations, the family photograph albums and her grandmother’s icons, <br>
                    including a huge one she had inherited from her uncle? They would not fit in the bags. She cried. She went to the garden and took pictures of every shrub, tree, and flower. <br>
                    At least she would keep the memories.
                </p>
                <p>
                    “Mom,” she said to her mother-in-law, “We’re leaving tomorrow. Have you packed?” <br>
                </p>
                <p>
                    “I’m not going. My life, my home, my husband’s grave are all in this village. <br>
                    Who will take care of them if I’m gone? I was born here and I will die here. You go. Take my granddaughters to peace, quiet, and happiness. <br>
                    Your whole lives are before you.” <br>
                </p>
                <p>
                    They could not leave her. So, they stayed! <br>
                </p>
                <p>
                    Once they decided to stay, Oksana’s whole view of the world changed. <br>
                    Their problems united them with their neighbours. They supported each other. <br>
                    When the electricity went out for forty days, her husband and his friends found a generator, <br>
                    repaired it, and found fuel. In the evenings, everyone came to their house to charge their batteries. People shared food. <br>
                    In the evening they took buckets of milk from the cows in the village to the street. Children, adults, and even cats and dogs drank as much milk as they wanted. <br>
                </p>
                <p>
                    Before then, they used to spend their evenings in front of the TV or the computer. <br>
                    Now they sat in the street and talked and exchanged advice. They figured out how to help each other clean a well. <br>
                    They organized water delivery to the elderly. They found medicines for people in need. <br>
                </p>
                <p>
                    A pair of storks stayed in the village, too. People say they bring good luck. But to Oksana they brought joy. <br>
                    Despite the scorching summer heat, the storks hatched five chicks. They were not afraid of gunfire. <br>
                    They did not abandon their nest, they stayed and survived, just like the people of the village. <br>
                    In the autumn, the storks left… but can you believe it? They came back the next spring!
                </p>
