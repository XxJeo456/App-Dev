                <h2>The House that Love Built</h2>
                <h3>By : William W. Price</h3>
                <hr>
                <p>
                   This story is set after World War I, where a whole village had been destroyed by the fighting.
                </p>
                
                <p>
                   Marie woke to blackness, her small body shivering from the damp cold. <br>
                   She rearranged her bed of rags and burlap on the dirt floor. <br>
                   The same nightmare jolted her from sleep every night, and closed around her like a dark cloud.
                </p>
                
                <p>
                   The dream always started pleasantly in her beloved village, walking from her home with Maman and Grandmere down the narrow street. <br>
                   Bright flowers waved under windows, and the sun gleamed on the tall church steeple.
                </p>
                
                <p>
                   Then she would begin shivering as a frightening gleam of guns crept toward their village. <br>
                   Maman and Grandmere would pull her into a clump of trees, where they would flattened themselves against the ground. <br>
                   Blue-uniformed soldiers would pass in waves. <br>
                   Guns! <br>
                   Fighting! <br>
                   Explosions! <br>
                   Screams! <br>
                   Fire! <br>
                   When they were gone, the village was also gone. <br>
                   Marie, her mother, and her grandmother would take shelter in the old fruit cellar.
                </p>
                
                <p>
                   Marie fell back into a fitful sleep. <br>
                   Soldiers marched on and on. <br>
                   After French soldiers in blue came Germans in green, then Americans in khaki who laughed and gave pennies to the children, but still left the village in ruins.
                </p>
                
                <p>
                   When Marie woke again, sunlight shone across the fruit cellar. <br>
                   Hearing new sounds, she wondered what they could be. <br>
                   “Maman, have the soldiers come back?” she asked.
                </p>
                
                <p>
                   “No, my dear. <br>
                   Go see who has arrived,” Maman looked strangely pleased.
                </p>
                
                <p>
                   When Marie climbed the fruit-cellar steps she saw new men in grey uniforms with a red and black star on each sleeve and on their caps.
                </p>
                
                <p>
                   “Oh, Maman!” cried Marie after watching for several minutes. <br>
                   “They carry saws and hammers, not guns. <br>
                   They are building houses!” <br>
                   Marie had thought they were soldiers because they wore uniforms, but they were British and American Quakers.
                </p>
                
                <p>
                   Marie ran back down the cellar steps and grabbed a sock holding six pennies, the family’s only money. <br>
                   She hurried back up, anxious and hopeful. <br>
                   She ran to the men’s leader and timidly showed him her pennies. <br>
                   “Sir, can you build me a house for six cents?”
                </p>
                
                <p>
                   Surprised, the man asked her to repeat her question, then replied quite seriously, <br>
                   “Well, Mademoiselle, we’ll see what we can do.”
                </p>
                
                <p>
                   Marie watched daily to see what would happen.
                </p>
                
                <p>
                   Would they build her family a house before leaving? <br>
                   Just when she was giving up, Marie received her answer: “Yes!” <br>
                   It was small just like the others, but to her it was the most beautiful house in the world.
                </p>
                
                <p>
                   When it was finished, the men’s leader offered the front door key to Marie with great ceremony, saying <br>
                   “Miss, the key.”
                </p>
                
                <p>
                   Marie started to open the door, all the village looking on. <br>
                   But then she stopped. <br>
                   She ran down into the cellar and came back with her six pennies. <br>
                   Now that the house was finished, they looked too few, but they were all she had. <br>
                   She counted them into the leader’s hand. <br>
                   With a huge smile, Marie went inside with her family. <br>
                   Love and gratitude filled their hearts and their new home.
                </p>                
