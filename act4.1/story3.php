                <h2>Boot Under the Bed</h2>
                <h3>By : Murry Engle Lauser, Set in the United Kingdom</h3>
                <hr>
                <p>
                   In spite of her heavy wool skirt and shawl, Elizabeth Fry was chilled from her day in the drafty Bristol women’s prison. <br>
                   “It’s not fit for anyone, no matter what their crime! <br>
                   But those poor children! <br>
                   What could I do to help occupy their minds and hands?” Elizabeth thought as she climbed the winding stairs in the old stone inn.
                </p>

                <p>
                   When she opened the door to her room, she sensed something strange. <br>
                   A dresser drawer was ajar, and a candle tipped over. <br>
                   Just visible under the bed was a man’s boot.
                </p>

                <p>
                   She gasped. <br>
                   What should she do? <br>
                   As she quietly closed the drawer and picked up the candle, she reached a decision. <br>
                   She knelt beside the boot, hearing someone breathing under the bed. <br>
                   “Dear God,” she began, “please forgive this man. <br>
                   May thy goodness enter his heart and help him to improve his ways.” <br>
                   Her voice was so kind. <br>
                   The boot stirred. <br>
                   “This man is confused and needs thy guidance to stop stealing.”
                </p>

                <p>
                   The man crawled out from under the bed. <br>
                   He was thin and unshaven. <br>
                   “Why are you praying for me?” he asked gruffly. <br>
                   “Why don’t you call the innkeeper and get it over with?”
                </p>

                <p>
                   “God is the only one I’ll call,” said Elizabeth. <br>
                   She was still afraid but looked at him kindly, “Thee must have a reason to come to my room.” <br>
                   His shoulders drooped. <br>
                   “Can’t thee tell me?” she asked. <br>
                   The man remained silent. <br>
                   Elizabeth waited.
                </p>

                <p>
                   “I’ve been hungry for days, Ma’am,” he said at last. <br>
                   “I stole scraps of food, but it’s not enough. <br>
                   I need money for a good meal and a warm coat too.”
                </p>

                <p>
                   “I’m glad thee came to my room,” said Elizabeth.
                </p>

                <p>
                   The man looked at her in amazement. <br>
                   He had never been treated so kindly, even when he worked as a stagecoach footman. <br>
                   Elizabeth pulled a heavy sweater from the drawer. <br>
                   “This was my husband’s,” she said. <br>
                   “I think it will fit thee. <br>
                   Now let’s go downstairs for dinner.”
                </p>

                <p>
                   “You’re sure good to me,” he said. <br>
                   “You could have sent me to prison—or will you?” <br>
                   His eyes darted wildly toward the window.
                </p>

                <p>
                   “No,” she said. <br>
                   “I know too much about prisons to send anyone there.” <br>
                   Seeing his puzzlement, she added, “I’ll tell thee at dinner.”
                </p>

                <p>
                   While they ate boiled mutton and potatoes, she told him about her prison work and he told her about his troubles. <br>
                   First he had gone to prison for an unpaid debt, then for stealing, and then for a false accusation. <br>
                   Since his release, he could not get a job. <br>
                   His clothes were worn and dirty, so no one trusted him. <br>
                   He wanted to stay out of prison so much he almost starved before coming to her room. <br>
                   Elizabeth had heard similar stories from women in prison.
                </p>

                <p>
                   In her practical way, Elizabeth gave the man soap, clean clothes, and help in finding a job. <br>
                   He left with strength in his body and hope in his heart. <br>
                   Elizabeth felt a deep sense of peace, grateful she had responded with love rather than fear.
                </p>                
