                <h2>The Healing Power of Forgiveness</h2>
                <h3>By : Aba Gayle — Set in U.S.A.</h3>
                <hr>
                <p>
                   Detective Landry was gentle as he spoke those terrible words: <br>
                   “I’m sorry, but your daughter, Catherine, is dead. <br>
                   She was murdered, stabbed to death.”
                </p>

                <p>
                   My heart broke. <br>
                   My brain couldn’t think. <br>
                   Nothing was real. <br>
                   Surely I would wake to find the nightmare was over. <br>
                   I couldn’t let anyone hug me for fear I would break. <br>
                   I couldn’t cry for fear someone might hear. <br>
                   With the shower running full blast, I screamed and screamed and screamed. <br>
                   People thought I was fine, but a deep, dark rage boiled. <br>
                   All I thought about was revenge for the death of my beloved child.
                </p>

                <p>
                   Douglas Mickey was arrested, tried, convicted, and sentenced to death. <br>
                   People said once this villain was executed, I would be well again. <br>
                   Not knowing any better, I believed them. <br>
                   So, I waited, and I hated.
                </p>

                <p>
                   After eight long years of darkness, I took my first step toward healing. <br>
                   In a meditation course I sat, quieted my head, and was present. <br>
                   I began taking mother to church. <br>
                   I found not only myself, but the image of God in me. <br>
                   I became aware of being a beloved child of God.
                </p>

                <p>
                   I saw an interview with a Jewish holocaust survivor. <br>
                   He forgave not only the German people, but the actual guards who killed his whole family. <br>
                   When I heard his testimony of forgiveness, something in me became clear. <br>
                   I thought perhaps I could forgive the man who murdered Catherine.
                </p>

                <p>
                   One evening a friend suggested that I let the murderer know of my intent. <br>
                   I was outraged! <br>
                   No way would I communicate with him. <br>
                   This was between God and me.
                </p>

                <p>
                   But as I drove home, I heard a voice, “You must forgive him, and let him know!” <br>
                   The voice was so loud and convincing I didn’t sleep. <br>
                   At four in the morning, I found myself typing a letter to the man who murdered Catherine.
                </p>

                <p>
                   I can still feel the shivers going down my spine as I closed the mailbox. <br>
                   All the anger, rage, and lust for revenge simply vanished. <br>
                   In its place, was the most wonderful feeling of joy and peace. <br>
                   I knew, in that holy instant, no one had to be executed for me to be healed. <br>
                   I had been healed by the simple act of offering forgiveness.
                </p>

                <p>
                   To my surprise, I received a gentle and kind reply. <br>
                   Douglas expressed sorrow for his crime, adding that he understood how empty such words might sound. <br>
                   He wrote, “Gayle, your letter meant more to me than I can ever tell you. <br>
                   The knowledge that I inflicted such terrible pain on you was a burden my heart and soul could not bear. <br>
                   Your letter of forgiveness released me of that pain. <br>
                   Knowing you were able to deal with Catherine’s death and find new sources of love and wisdom gave me exquisite pleasure and released my soul’s agony. <br>
                   I would gladly give my life, this instant, if it would in any way change that terrible night.” <br>
                   I realized that the night Catherine lost her life, Douglas lost his future.
                </p>    
