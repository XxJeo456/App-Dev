                <h2>Across the Fence</h2>
                <h3>By : Hanan Schlesinger, Israel</h3>
                <hr>
                <p>
                    It was a miracle that we ever met at all. <br>
                </p>
                <p>
                    I had lived in Israel for 35 years, but didn’t really know any Palestinians when, <br>
                    a few years ago, I received a visitor to my home who offered me an opportunity to meet neighbors who had been unknown and invisible to me. <br>
                    I could go to a meeting a 20-minute walk from my home in the West Bank. I told my wife, “I’m going to meet some Palestinians.” <br>
                </p>
                <p>
                    “Don’t go,” she cried, “it’s too dangerous!” <br>
                </p>
                <p>
                    I feared she was right, but went anyway. <br>
                </p>
                <p>
                   Walking through Palestinian vineyards and fields, <br>
                   I could feel my heart pounding. When I arrived, I was stunned. <br>
                   Twenty Israelis and twenty Palestinians were standing and sitting in small circles, <br>
                   talking and eating, together. In thirty-five years in Gush Etzion, I had never seen anything like it. <br>
                </p>
                <p>
                    I saw a Palestinian woman dressed in brown, from head to toe, standing alone. <br>
                    I greeted her. We both marveled, “I can’t believe I’m talking with you.” <br>
                </p>
                <p>
                    “I know,” she replied, “I can’t believe I’m talking with you either.”  <br>
                </p>
                <p>
                    She called her son, Yazin. He was wearing a windbreaker with Seeds of Peace on his jacket. <br>
                    I reeled again as the voice in my mind said, “Palestinians don’t know the word peace…” <br>
                </p>
                <p>
                    Abruptly, I asked out loud, “What is Seeds of Peace?” <br>
                </p>
                <p>
                   Yazin explained that it was a camp in Maine, in the United States, that brings Palestinian and Israeli young people together to meet each other. <br>
                   Here was a Palestinian teenager explaining to me, the Rabbi, the ideas and practices of peace and reconciliation!
                </p>

                <p>
                   She then introduced Jamal, her husband. <br>
                   They lived in Beit Ummar, a neighboring village, very close and yet so far away. <br>
                   I heard a voice in my mind say, “That’s where terrorists come from–blood thirsty people, who throw rocks and want to kill us.” <br>
                   I also heard how my wife would say, “Go to Beit Ummar!” in frustration, meaning “Go to hell!”
                </p>

                <p>
                   As I caught my breath, Jamal took out a smartphone. <br>
                   I reeled again; smartphones were quite new at the time, yet Palestinians have smartphones? <br>
                   Jamal opened Google Maps; I’d never seen Google Maps. <br>
                   Jamal pointed out his house. <br>
                   It was directly across the fence from mine! <br>
                   Jamal and I were literally neighbors, with an impermeable fence between us. <br>
                   Out of nowhere Jamal said, “When my kids see people like you, with yarmulkes (Jewish head coverings) and long beards, they cry.”
                </p>

                <p>
                   Jolted from my thoughts, I asked, “Why? <br>
                   What’s so scary about yarmulkes and beards?”
                </p>

                <p>
                   He caught his breath, surprised and confused. <br>
                   It seemed all too obvious, “Well… because people with yarmulkes and beards carry submachine guns and kill our kids!”
                </p>

                <p>
                   It took a minute to sink in. <br>
                   It was like looking at myself in a mirror, seeing how Jamal and his children saw me. <br>
                   Jewish people liked to go outside the fence to walk in the land of the biblical stories where Abraham, Isaac, and Jacob had walked. <br>
                   But of course we carried submachine guns when we went, to protect ourselves, because it was dangerous.
                </p>

                <p>
                   “You know, Jamal, we carry guns because we’re afraid of you,” I told him.
                </p>

                <p>
                   “No,” he replied, “It is we who are afraid of you.”
                </p>

                <p>
                   My mind was racing. <br>
                   I wondered why we had so much fear of each other. <br>
                   Was it because we lived so close together, Palestinian and Israeli, but had no contact? <br>
                   We lived in different areas, worshipped in different ways in different buildings, had different media, newspapers, radio and television, in different languages, so we got different news, and therefore we really did live in different worlds, with different understandings, and different realities just across the fence.
                </p>

                <p>
                   But as I saw these faces of human beings and listened to their stories, we weren’t that different.
                </p>

                <p>
                   I was not sure what happened to me that evening, but whatever it was, it changed me forever.
                </p>

                <p>
                   Hanan Schlesinger is an Orthodox Rabbi, Zionist Settler, and Director of International Relations for Roots/Judur/Shorashim, a Palestinian Israeli Grassroots Initiative for Understanding, Non-violence, and Transformation on land owned by the Awaad family in the heart of Gush Etzion.
                </p>
